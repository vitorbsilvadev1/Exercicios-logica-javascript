/* Pergunte ao usuário dois valores e exiba o produto deles.
 */

let primeiroNumero = parseInt(prompt("Digite o 1° número: "))
let segundoNumero = parseInt(prompt("Digite o 2° número: "))

let produto = primeiroNumero * segundoNumero

alert(`${primeiroNumero} * ${segundoNumero} = ${produto}`)


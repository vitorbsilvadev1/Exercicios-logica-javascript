/* Pergunte ao usuário dois valores e mostre o resultado da divisão do
primeiro pelo segundo.
 */

let primeiroNumero = parseInt(prompt("Digite o 1° número: "))
let segundoNumero = parseInt(prompt("Digite o 2° número: "))

let divisao = primeiroNumero / segundoNumero

alert(`${primeiroNumero} / ${segundoNumero} = ${divisao}`)


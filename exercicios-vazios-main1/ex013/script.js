/* Pergunte ao usuário dois valores e exiba esses valores trocados.
 */

let primeiroNumero = parseInt(prompt("Digite o 1° número: "))
let segundoNumero = parseInt(prompt("Digite o 2° número: "))
let num = primeiroNumero

alert(`1° valor: ${primeiroNumero = segundoNumero}`)
alert(`2° valor: ${segundoNumero = num}`)